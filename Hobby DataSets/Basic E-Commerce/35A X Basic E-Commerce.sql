	prodid, 
	ROUND(SUM(final_amount)::numeric,2) AS total_revenue, 
	COUNT(DISTINCT custid) AS distinct_customers,
	ROUND((SUM(final_amount)::numeric/NULLIF(COUNT(DISTINCT custid),0)),2) AS revenue_per_customer
