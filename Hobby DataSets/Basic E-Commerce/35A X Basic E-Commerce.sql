1.	Calculate number of distinct products purchased per customer.
2.	Calculate total transactions per customer.
3.	Return customers who purchased at least 5 distinct products.
*/
--
SELECT
    c.custid,
    c.customer_name,
    COUNT(DISTINCT t.prodid) AS distinct_products,
    COUNT(*) AS total_transactions
FROM transactions t
JOIN customers c
ON t.custid = c.custid
GROUP BY c.custid, c.customer_name
HAVING COUNT(DISTINCT t.prodid) >= 5;

