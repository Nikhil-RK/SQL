    custid, 
    COUNT(*) AS transaction_count,
    ROUND(SUM(final_amount)::numeric,2) AS total_spent_last_3_months
FROM transactions
WHERE transaction_date <= DATE '2024-12-31' - INTERVAL '3 months'
