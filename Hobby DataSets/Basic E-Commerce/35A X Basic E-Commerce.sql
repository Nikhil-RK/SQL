-- Question No. 2
/*
1.	Calculate total transactions per city.
2.	Calculate total revenue per city.
3.	Compute average revenue per transaction and rank cities.

*/
--
WITH city_metrics AS (
SELECT
    c.city,
    COUNT(*) AS total_transactions,
    ROUND(SUM(t.final_amount)::numeric,2) AS total_revenue,
    ROUND(AVG(t.final_amount)::numeric,2) AS avg_revenue_per_transaction
FROM transactions t
JOIN customers c
ON t.custid = c.custid
GROUP BY c.city
)

SELECT *,
DENSE_RANK() OVER (
    ORDER BY avg_revenue_per_transaction DESC
) AS city_rank
FROM city_metrics;
