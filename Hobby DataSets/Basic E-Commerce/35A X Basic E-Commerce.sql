2.	Compare preferred vs actual.
3.	Count matches.
*/
--
WITH CTE AS (
SELECT 
	c.preferredpaytype,
	t.paymentid,
	(CASE WHEN c.preferredpaytype = t.paymentid THEN 1 ELSE 0 END) AS identifier
FROM transactions AS t
INNER JOIN customers AS c
ON c.custid = t.custid
INNER JOIN payment_types AS p
ON p.paytid = t.paymentid)

SELECT
	COUNT(CASE WHEN identifier = 1 THEN 1 END) AS matchedCount
FROM CTE


