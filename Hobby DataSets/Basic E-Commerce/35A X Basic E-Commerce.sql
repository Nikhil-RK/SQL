/*
1. Calculate the number of transactions for each payment type. 
2. Calculate the total revenue generated from each payment type. 
3. Using these two values, determine the average transaction value for each 
	payment type and rank them in descending order.
*/
--
SELECT
	p.paytid,
    p.paytype AS paymentType,
    COUNT(*) AS transaction_count,
    ROUND(SUM(t.final_amount)::numeric, 2) AS total_revenue,
    ROUND(AVG(t.final_amount)::numeric, 2) AS avg_transaction_value,
	DENSE_RANK() OVER (ORDER BY ROUND(AVG(t.final_amount)::numeric, 2) DESC) AS rnk
FROM transactions AS t
INNER JOIN payment_types AS p
    ON p.paytid = t.paymentid
GROUP BY p.paytid, p.paytype;

