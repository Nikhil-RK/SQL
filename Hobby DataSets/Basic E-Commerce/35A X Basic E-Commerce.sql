SELECT
    p.prodname, p.prodcat,
    ROUND(SUM(t.final_amount)::numeric,2) AS total_revenue
FROM transactions t
JOIN products p
ON t.prodid = p.prodid
GROUP BY p.prodname, p.prodcat
),
calc AS (
SELECT prodname, prodcat, total_revenue,
	DENSE_RANK() OVER (PARTITION BY prodcat ORDER BY total_revenue DESC) AS rnk
FROM product_revenue
)
