-- Question No. 17
/*
1.	Calculate total revenue per day.
2.	Use a window function to get previous day's revenue.
3.	Calculate day-over-day revenue change.
*/
--
WITH currentDayRevenue AS ( 
SELECT
	transaction_date,
	SUM(final_amount::numeric) AS curDAYR
FROM transactions
GROUP BY transaction_date)

SELECT transaction_date, curDAYR,
	COALESCE(LAG(curDAYR) OVER (ORDER BY transaction_date), 0) AS prevDAYR,
	(curDAYR::numeric - LAG(curDAYR) OVER (ORDER BY transaction_date)::numeric) AS change
FROM currentDayRevenue


