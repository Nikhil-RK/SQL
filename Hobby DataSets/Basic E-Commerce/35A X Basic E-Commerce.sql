-- Question No. 3
/*
1.	Calculate total quantity sold per product.
2.	Calculate total revenue per product.
3.	Determine revenue per unit sold and rank products.

*/
--
WITH product_metrics AS (
SELECT 
    p.prodid,
    p.prodname,
    SUM(t.quantity) AS total_quantity,
    SUM(t.final_amount) AS total_revenue
FROM transactions t
JOIN products p
ON t.prodid = p.prodid
GROUP BY p.prodid, p.prodname
)

SELECT
    *,
    ROUND(total_revenue / NULLIF(total_quantity,0),2) AS revenue_per_unit,
    DENSE_RANK() OVER (
        ORDER BY total_revenue / NULLIF(total_quantity,0) DESC
    ) AS product_rank
FROM product_metrics
ORDER BY revenue_per_unit DESC;
