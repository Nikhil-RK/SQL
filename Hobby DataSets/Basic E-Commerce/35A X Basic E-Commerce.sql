-- Question No. 11
/*
1.	Join customers, transactions, and products.
2.	Calculate revenue per city and category.
3.	Identify top category per city.
*/
--
WITH CTE AS (
SELECT 
	c.city, p.prodcat,
	ROUND(SUM(t.final_amount::numeric),2) AS total_revenue,
	RANK() OVER (PARTITION BY c.city ORDER BY SUM(t.final_amount::numeric) DESC) AS rnk
FROM transactions AS t
INNER JOIN customers AS c
ON t.custid = c.custid
INNER JOIN products AS p
ON t.prodid = p.prodid
GROUP BY c.city, p.prodcat )

SELECT city, prodcat, total_revenue
FROM CTE 
WHERE rnk = 1
ORDER BY total_revenue DESC;

